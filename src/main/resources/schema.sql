CREATE TABLE IF NOT EXISTS employee(
 employee_id INT PRIMARY KEY,
 employee_name VARCHAR(50),
 age INT
);

CREATE TABLE IF NOT EXISTS m_pc(
 name VARCHAR(45)
 ,os VARCHAR(45)
 ,cpu VARCHAR(45)
 ,ghz DECIMAL(10.0)
 ,gpu VARCHAR(45)
 ,ram INT
 ,hddCapa INT
 ,byt VARCHAR(45)
 ,ssdCapa INT
 ,byt2 VARCHAR(45)
 ,eth CHAR(45)
 ,strWifi VARCHAR(45)
 ,resolution VARCHAR(45)
 ,lcd DECIMAL(10.0)
 ,manufacturers VARCHAR(45)
 ,release VARCHAR(45)
 ,description VARCHAR(100)
);